export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.rating >= 4.6).slice(0, 8);
}

export function getTrendingProducts(): Product[] {
  return products.filter((p) => p.reviewCount >= 400).slice(0, 4);
}
